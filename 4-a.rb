def find_in_rows(matrix, string)
  matrix.map do |row|
    find_string_in_array(row, string)
  end.sum
end

def find_in_columns(matrix, string)
  matrix.transpose.map do |column|
    find_string_in_array(column, string)
  end.sum
end

def find_all_diagonals(matrix)
  return [] if matrix.empty?

  n = matrix.length     # number of rows
  m = matrix[0].length  # number of columns
  diagonals = []

  # Find all diagonals starting from first row
  (0...m).each do |col|
    diagonal = []
    r, c = 0, col

    # Follow diagonal down-right
    while r < n && c < m
      diagonal << matrix[r][c]
      r += 1
      c += 1
    end
    diagonals << diagonal if diagonal.length > 1
  end

  # Find all diagonals starting from first column (except first element)
  (1...n).each do |row|
    diagonal = []
    r, c = row, 0

    # Follow diagonal down-right
    while r < n && c < m
      diagonal << matrix[r][c]
      r += 1
      c += 1
    end
    diagonals << diagonal if diagonal.length > 1
  end

  # Now find all secondary diagonals (top-right to bottom-left)
  # Start from first row
  (0...m).each do |col|
    diagonal = []
    r, c = 0, col

    # Follow diagonal down-left
    while r < n && c >= 0
      diagonal << matrix[r][c]
      r += 1
      c -= 1
    end
    diagonals << diagonal if diagonal.length > 1
  end

  # Find secondary diagonals starting from last column (except first element)
  (1...n).each do |row|
    diagonal = []
    r, c = row, m - 1

    # Follow diagonal down-left
    while r < n && c >= 0
      diagonal << matrix[r][c]
      r += 1
      c -= 1
    end
    diagonals << diagonal if diagonal.length > 1
  end

  diagonals
end

def find_in_diagonals(matrix, string)
  diagonals = find_all_diagonals(matrix)

  diagonals.map do |s|
    find_string_in_array(s, string)
  end.sum
end

def find_string_in_array(array, string)
  joined_array = array.join('')

  joined_array.scan(/#{string}/).length + joined_array.scan(/#{string.reverse}/).length
end

